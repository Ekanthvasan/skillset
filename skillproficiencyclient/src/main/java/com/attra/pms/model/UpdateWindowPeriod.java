package com.attra.pms.model;

public class UpdateWindowPeriod {
	private String buID;
	private String accID;
	private String projectID;
	private String empEmailID;
	private String triggerPeriod;
	private String triggerDate;
	private String organisation;
	
	public String getBuID() {
		return buID;
	}
	public void setBuID(String buID) {
		this.buID = buID;
	}
	public String getAccID() {
		return accID;
	}
	public void setAccID(String accID) {
		this.accID = accID;
	}
	public String getProjectID() {
		return projectID;
	}
	public void setProjectID(String projectID) {
		this.projectID = projectID;
	}
	public String getEmpEmailID() {
		return empEmailID;
	}
	public void setEmpEmailID(String empEmailID) {
		this.empEmailID = empEmailID;
	}
	public String getTriggerPeriod() {
		return triggerPeriod;
	}
	public void setTriggerPeriod(String triggerPeriod) {
		this.triggerPeriod = triggerPeriod;
	}
	public String getTriggerDate() {
		return triggerDate;
	}
	public void setTriggerDate(String triggerDate) {
		this.triggerDate = triggerDate;
	}
	public String getOrganisation() {
		return organisation;
	}
	public void setOrganisation(String organisation) {
		this.organisation = organisation;
	}
	
	
	
	

}
